/**
 * Bir diziyi yerinde karıştırmak için Fisher-Yates (aka Knuth) Shuffle algoritması.
 * @param {Array} array Karıştırılacak dizi.
 */
function shuffleArray(array) {
    const newArray = [...array];
    for (let i = newArray.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [newArray[i], newArray[j]] = [newArray[j], newArray[i]];
    }
    return newArray;
}

// Ana sayfada gösterilecek görselleri dinamik olarak oluşturalım
function createMainPageImages() {
    // İlk (sabit) resmi diziden ayır
    const firstImage = allGalleryImages[0];

    // Geri kalan resimleri al
    const restOfTheImages = allGalleryImages.slice(1);

    // Geri kalan resimleri karıştır
    const shuffledRest = shuffleArray(restOfTheImages);

    // Ana sayfada toplam 6 resim göstereceğimizi varsayalım.
    // Karıştırılmış resimlerden ilk 5 tanesini al.
    const randomImages = shuffledRest.slice(0, 8);

    // Sabit ilk resmi, rastgele seçilen 5 resmin başına ekle
    return [firstImage, ...randomImages];
}

